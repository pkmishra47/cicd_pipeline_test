package org.apache.nifi.processors.daxoperation.dm;

public class CustomerDetails {
    private String MobileNo;
    private String Comm_addr;
    private String Del_addr;
    private String FirstName;
    private String LastName;
    private String Uhid;
    private String City;
    private String PostCode;
    private String MailId;
    private String Age;
    private String CardNo;
    private String PatientName;
    private String Latitude;
    private String Longitude;


    public String getMobileNo() {
        return MobileNo;
    }

    public void setMobileNo(String mobileNo) {
        MobileNo = mobileNo;
    }

    public String getComm_addr() {
        return Comm_addr;
    }

    public void setComm_addr(String comm_addr) {
        Comm_addr = comm_addr;
    }

    public String getDel_addr() {
        return Del_addr;
    }

    public void setDel_addr(String del_addr) {
        Del_addr = del_addr;
    }

    public String getFirstName() {
        return FirstName;
    }

    public void setFirstName(String firstName) {
        FirstName = firstName;
    }

    public String getLastName() {
        return LastName;
    }

    public void setLastName(String lastName) {
        LastName = lastName;
    }

    public String getUhid() {
        return Uhid;
    }

    public void setUhid(String uhid) {
        Uhid = uhid;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

    public String getPostCode() {
        return PostCode;
    }

    public void setPostCode(String postCode) {
        PostCode = postCode;
    }

    public String getMailId() {
        return MailId;
    }

    public void setMailId(String mailId) {
        MailId = mailId;
    }

    public String getAge() {
        return Age;
    }

    public void setAge(String age) {
        Age = age;
    }

    public String getCardNo() {
        return CardNo;
    }

    public void setCardNo(String cardNo) {
        CardNo = cardNo;
    }

    public String getPatientName() {
        return PatientName;
    }

    public void setPatientName(String patientName) {
        PatientName = patientName;
    }

    public String getLatitude() {
        return Latitude;
    }

    public void setLatitude(String latitude) {
        Latitude = latitude;
    }

    public String getLongitude() {
        return Longitude;
    }

    public void setLongitude(String longitude) {
        Longitude = longitude;
    }
}
